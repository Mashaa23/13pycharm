def onlineclass(name, age, gender):
    print(f"My name is {name} and I am {age} years old and I am {gender}")

onlineclass("John", 18, "male")
onlineclass("Judie", 25, "female")

def flowerbio(name, color, location):
    print(f"This flower is called {name} and it is {color} in color and found in {location}")

flowerbio("Rose", "blue", "Australia") 