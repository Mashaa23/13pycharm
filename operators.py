# arithmetic operators, assignment operators, logical operators, comparison operators


num = 43 # first operand
nu2 = 12 # second operand

# Arithmetic operator
print(f"The sum of {num} and {nu2} is {num + num}")
print(f"The difference of {num} and {nu2} is {num - num}")
print(f"The division of {num} and {nu2} is {num / num}")
print(f"The multiplication of {num} and {nu2} is {num * num}")
print(f"The remainder of {num} and {nu2} is {num % num}")
print(f"The exponent of {num} and {nu2} is {num ** num}")
print(f"The floor of {num} and {nu2} is {num // num}")

